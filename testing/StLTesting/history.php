                                <p>It was suggested that the Warner clock bell at St Lawrence could be recast as the 5th of
                                    the intended ring of eight. The Diocesan Bells Adviser agreed to this provided the
                                    inscription on the new bell recorded that it had been cast from the 1893 bell, but in
                                    the event there were objections from within the church community and the clock bell was
                                    retained. The five Taylor bells were augmented to eight by the addition of new treble,
                                    2nd and 5th bells, cast at Taylor's early in 1999. As five of the bells were old, it was
                                    decided to inscribe all eight by incision, and each bell now bears the dedication of a
                                    church or religious house which once stood within the present-day parish of St
                                    Lawrence. </p>
                                <p>The eight bells and their fittings were delivered on 29th March 1999 and placed on
                                    display in the church. They were christened on Easter Sunday and their hanging commenced
                                    on 10th April. The hanging was completed and the bells ready for the try-out by 29th
                                    April. They were dedicated by the Dean of York, The Very Reverend Raymond Furnell, on
                                    4th July 1999.</p>
