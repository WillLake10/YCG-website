                                </div>
                                <p class="historyHeaders">Bells of St Lawrence in and after the Civil War</p>
                                <p>The old church had a ring of three bells at the time of the Civil War, but the church and
                                    its tower were badly damaged by cannon ball fire during the 1644 siege of York and in
                                    1657 the City Council ordered that the bells of St Lawrence should be weighed and
                                    secured by Alderman Topham and Edmund Giles as the tower of the church was in danger of
                                    collapse. The City Corporation would assume responsibility for their safe-keeping. The
                                    bells were subsequently used as a supply of scrap for other churches in the city: the
                                    metal from the tenor, which weighed 4-0-7 and was cracked, was used in 1657 when two new
                                    bells were cast for the Minster (q.v.). The metal from the two smaller bells was used in
                                    1659 in casting a treble to augment the ring at Holy Trinity, King's Square (q.v.), to
                                    four. The task of repairing St Lawrence did not begin until 1669.</p>
                                <p class="historyHeaders">Proposals to add Bells back into St Lawrence</p>
                                <p>In 1944 there was a proposal (for which a faculty dated 22nd August 1944 was granted) to
                                    hang the back three bells from the redundant church of St John, Ouse Bridge (q.v.), in
                                    an oak frame in the tower of St Laurence. However, in the event this did not go ahead
                                    and all six bells were rehung at St John's in 1955 as part of the conversion of the
                                    building into The York Institute of Architectural Study.</p>
                                <p>In October 1997 The York Bell Fund put a proposal to install a ring of bells before the
                                    St Lawrence's PCC. The PCC agreed to this proposal on condition that they did not have
                                    to finance the scheme. The ringers agreed to this, and the tower was subsequently
                                    surveyed by Adrian Dempster, a member of the Towers and Belfries Committee of the
                                    Central Council of Church Bell Ringers, who advised that it could hold a ring of eight
                                    bells with a tenor of up to 10 cwt.</p>
                                <div class="imgTextWrapRight figBox">
                                    <img src="images/st_laurence_bells.jpg" style="max-width: 100%" width="300">
                                    <div class="figureCap">The 8 bells on display after delivery</div>
                                </div>
                                <p class="historyHeaders">Procuring the New Ring</p>
                                <p>Five bells to form the basis of the ring (the 3rd, 4th, 6th, 7th, and tenor) were already
                                    in the possession of The York Bell Fund. These, along with £5,000, were given
                                    immediately by The Fund. One of the bells (the 6th) had been part of a duplicate set of
                                    bells cast for the Albany City Hall Carillon in the USA, but had not been used there and
                                    had subsequently been purchased from the founders (Taylor's) in 1989. The bell was tuned
                                    by Taylor's to make it accord with the other four bells acquired for the ring. These
                                    bells had formerly been the four largest of a five-bell Westminster clock chime, cast by
                                    Taylor's in 1947 for G and F Cope, Clockmakers. The bells had been exhibited at the
                                    Olympia Exhibition, held in London in 1947, and had subsequently been sold and installed
                                    in Charrington's Brewery in the east end of London.</p>
                                <p>All five of the Charrington bells were subsequently sold back to Taylor's and later
                                    acquired by The York Bell Fund. The smallest bell was cast to a chiming scale, and was
                                    therefore too small and light to form part of a ring with the other four. It was sold
                                    for use as a single chiming bell, and is now at the church of St Augustine, Harehills,
                                    Leeds, where it is hung 'dead' in a frame affixed to the tower-cum-chimney-stack and is
                                    fitted with a lever clapper. </p>
                                <p>It is interesting to note that the four Charringtons bells were originally intended to
                                    form the basis of a ring of six for All Saints, North Street, and the current back four
                                    at North Street were intended as the back four of a six for All Saints, Pavement. They
                                    were given to North Street when the Pavement scheme fell through, and the bells intended
                                    for North Street were subsequently given to St Lawrence.</p>
                                <div class="imgTextWrapLeft figBox">
                                    <img src="images/stLBellsInFrame.jpg" style="max-width: 100%">
                                    <div class="figureCap">The bells once hung</div>
