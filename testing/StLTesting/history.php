                                <p class="historyHeaders">Background of the fitting of the bells</p>
                                <p>The present church of St Lawrence, standing just outside the city walls, was built in
                                    1881-3 to the designs of J.G. Hall of Canterbury. The church is the largest parish
                                    church in York. It consists of nave with clerestory, aisles and transepts, an apsidal
                                    chancel, and a north-western tower with spire. The tower and spire were not built until
                                    1892-3. A peal of eight bells were installed during 1999.</p>

                                <p>Before the ring of bells was installed a vast amount of work had to be undertaken so that
                                    the tower could accommodate them. Two new floors, copying the design of the tower's
                                    original first level floor were installed, and an opening sound control floor was also
                                    installed between the bell chamber and louvre openings. The staircase access was also
                                    modified and the entire tower was rewired.</p>
                                <div class="imgTextWrapLeft figBox">
                                    <img src="images/stLawrenceSpire1.jpg" style="max-width: 100%">
                                    <div class="figureCap">The old bell tower with new one being built behind <br>(1892-93)</div>
                                </div>
                                <p>The ring of eight were cast with flat tops and have fittings by Taylor's 1999, comprising
                                    cast iron headstocks (tenor curved, remainder straight) with bolted gudgeons, ball
                                    bearings, traditional wheels, stays and sliders.</p>
                                <p class="historyHeaders">The Dumb Bell</p>
                                <p>The dumb bell (which weighs approximately 4 cwt) consists of a second-hand traditional
                                    wheel, to which is attached a large steel plate which covers up the majority of the
                                    wheel. To this plate is welded a socket containing a Hastings stay, with the
                                    corresponding radius slide being located beneath the dumb bell. The gudgeons are
                                    attached through the plate, and swing in light-duty ball bearings. There are weights
                                    bolted to the bottom of the plate, on the opposite side to the stay, and there are holes
                                    at various other points on the plate so that extra weights can be attached to certain
                                    places to alter the behaviour of the dumb bell, particularly its speed of swing. The
                                    dumb bell was designed by David Potter and Graham Blackburn, and is an improved version
                                    of the dumb bell at St Wilfrid. The St Lawrence dumb bell was designed to have a slow
                                    handstroke to aid the teaching of the transfer from handstroke to backstroke, which many
                                    learners find difficult, and in this aim it succeeds admirably.</p>
                                <p class="historyHeaders">The Frame</p>
                                <p>The ring of eight and the dumb bell hang in a frame designed in 1998 by Peter Hayward and
                                    built in 1998-9 by Graham Blackburn. The bellframe is of the lowside type, and is
                                    fabricated using channel, flat, angle and bar section steel, all galvanised and mounted
                                    on a grillage of RSJs.</p>
                                <p class="historyHeaders">The Clock</p>
                                <p>The clock bell is maiden and has angular canons. Its inscription notwithstanding, it is
                                    clearly the work of Warner's. It is hung 'dead' in the chamber above the ringing bells,
                                    being attached to a beam which spans the tower on the southern side, and has not been
                                    turned to present new faces to the blows of its hammers. The fittings comprise an
                                    internal wrought iron clapper hanging from a cast-in staple, although from the fact that
                                    no clapper scars are visible it would appear that this has never been used, an Ellacombe
                                    hammer and a clock hammer.</p>
                                <div class="imgTextWrapRight figBox">
                                    <img src="images/clock.jpg" style="max-width: 100%">
                                    <div class="figureCap">The clock mechanism</div>
                                </div>
                                <p>The clock, built by Potts of Leeds in the same year as its bell was cast, is located in
                                    the same chamber as the bell. It was reconstructed by R. Newey of York in 1948, and was
                                    restored and converted to electric winding in late 2000 / early 2001 by The Cumbria
                                    Clock Company. At the time of the installation of the ringing bells the same company
                                    quoted for the provision of five clock hammers and an electronic control unit to sound
                                    the Westminster quarters and the hour, but in the event this part of the scheme was not
                                    implemented.</p>
                                <p class="historyHeaders">Disused Bell</p>
                                <p>The disused 1739 bell, which is by Edward Seller II of York, is mounted on the wall of
                                    the ringing room. It has chamfered canons, and has not been turned. It is fitted with a
                                    timber headstock with nailed ironwork and drive-in gudgeons, and retains what appears to
                                    be its original clapper (with bun ball), hanging from the cast-in staple. Apart from the
                                    clapper, the fittings appear to be of late nineteenth or early twentieth century date.
                                    The bell formerly hung in the tower of the old church. When the ring of eight was
                                    installed Taylor's quoted for hanging it 'dead' in the tower of the present church with
                                    a trigger-action clapper, but in the event this was not carried out.</p>
                                <div class="imgTextWrapLeft figBox">
                                    <img src="images/stLOld.gif" style="max-width: 100%">
